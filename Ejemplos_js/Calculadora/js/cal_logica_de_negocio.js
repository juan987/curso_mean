function MiCalculadora(){
    this.sumar = function(num1,num2){
        return num1+num2;
    }

    this.restar = function(num1,num2){
        return num1-num2;
    }

    this.multiplicar = function(num1,num2){
        return num1*num2;
    }

    this.dividir = function(num1,num2){
        return num1/num2;
    }
}